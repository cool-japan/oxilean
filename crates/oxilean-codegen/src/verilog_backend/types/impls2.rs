impl VlogWorklist {
    #[allow(dead_code)]
    pub fn new() -> Self {
        VlogWorklist {
            items: std::collections::VecDeque::new(),
            in_worklist: std::collections::HashSet::new(),
        }
    }
    #[allow(dead_code)]
    pub fn push(&mut self, item: u32) -> bool {
        if self.in_worklist.insert(item) {
            self.items.push_back(item);
            true
        } else {
            false
        }
    }
    #[allow(dead_code)]
    pub fn pop(&mut self) -> Option<u32> {
        let item = self.items.pop_front()?;
        self.in_worklist.remove(&item);
        Some(item)
    }
    #[allow(dead_code)]
    pub fn is_empty(&self) -> bool {
        self.items.is_empty()
    }
    #[allow(dead_code)]
    pub fn len(&self) -> usize {
        self.items.len()
    }
    #[allow(dead_code)]
    pub fn contains(&self, item: u32) -> bool {
        self.in_worklist.contains(&item)
    }
}

impl VerilogBackend {
    /// Create a new backend.
    ///
    /// * `system_verilog = false` → Verilog 2001
    /// * `system_verilog = true`  → SystemVerilog
    pub fn new(system_verilog: bool) -> Self {
        VerilogBackend { system_verilog }
    }
    /// Emit the keyword string for a `VerilogType`.
    ///
    /// In Verilog 2001 mode, `Logic` falls back to `reg`.
    pub fn emit_type(&self, ty: &VerilogType) -> String {
        match ty {
            VerilogType::Logic(w) if !self.system_verilog => VerilogType::Reg(*w).to_string(),
            other => other.to_string(),
        }
    }
    /// Emit a `VerilogExpr` as a Verilog source string.
    pub fn emit_expr(&self, expr: &VerilogExpr) -> String {
        expr.to_string()
    }
    /// Emit a complete Verilog / SystemVerilog module as a source string.
    pub fn emit_module(&self, module: &VerilogModule) -> String {
        let mut out = String::new();
        let ext = if self.system_verilog { "sv" } else { "v" };
        out.push_str(&format!(
            "// Generated by OxiLean VerilogBackend (.{ext})\n\n"
        ));
        out.push_str(&format!("module {}", module.name));
        if !module.params.is_empty() {
            out.push_str(" #(\n");
            for (i, (pname, default)) in module.params.iter().enumerate() {
                let comma = if i + 1 < module.params.len() { "," } else { "" };
                out.push_str(&format!("    parameter {pname} = {default}{comma}\n"));
            }
            out.push(')');
        }
        if !module.ports.is_empty() {
            out.push_str(" (\n");
            for (i, port) in module.ports.iter().enumerate() {
                let comma = if i + 1 < module.ports.len() { "," } else { "" };
                let width_str = range_suffix(port.width());
                let type_kw = if self.system_verilog
                    && matches!(port, VerilogPort::Input(..) | VerilogPort::Output(..))
                {
                    "logic"
                } else {
                    match port {
                        VerilogPort::Input(..) => "wire",
                        VerilogPort::Output(..) => "reg",
                        VerilogPort::InOut(..) => "wire",
                    }
                };
                out.push_str(&format!(
                    "    {dir} {type_kw}{width_str} {name}{comma}\n",
                    dir = port.direction(),
                    width_str = if width_str.is_empty() {
                        String::new()
                    } else {
                        format!(" {width_str}")
                    },
                    name = port.name(),
                ));
            }
            out.push(')');
        }
        out.push_str(";\n\n");
        for stmt in &module.body {
            out.push_str("    ");
            out.push_str(stmt);
            out.push('\n');
        }
        out.push_str("\nendmodule\n");
        out
    }
    /// Emit an `always` (Verilog 2001) or `always_ff`/`always_comb`
    /// (SystemVerilog) block.
    ///
    /// `sensitivity` examples:
    /// * `"posedge clk"` → clocked
    /// * `"*"`           → combinational (Verilog 2001)
    /// * `"comb"`        → `always_comb` in SV mode
    /// * `"ff posedge clk"` → `always_ff` in SV mode
    pub fn always_block(&self, sensitivity: &str, body: &str) -> String {
        if self.system_verilog {
            if sensitivity == "comb" {
                return format!("always_comb begin\n    {body}\nend");
            }
            if let Some(rest) = sensitivity.strip_prefix("ff ") {
                return format!("always_ff @({rest}) begin\n    {body}\nend");
            }
        }
        format!("always @({sensitivity}) begin\n    {body}\nend")
    }
    /// Emit a continuous assignment: `assign lhs = rhs;`
    pub fn assign_stmt(&self, lhs: &str, rhs: &str) -> String {
        format!("assign {lhs} = {rhs};")
    }
    /// Emit a non-blocking assignment: `lhs <= rhs;`
    pub fn nonblocking_assign(&self, lhs: &str, rhs: &str) -> String {
        format!("{lhs} <= {rhs};")
    }
    /// Emit a blocking assignment: `lhs = rhs;`
    pub fn blocking_assign(&self, lhs: &str, rhs: &str) -> String {
        format!("{lhs} = {rhs};")
    }
    /// Emit an `if`/`else` construct inside a procedural block.
    pub fn if_else(&self, cond: &str, then_body: &str, else_body: Option<&str>) -> String {
        match else_body {
            Some(eb) => {
                format!(
                    "if ({cond}) begin\n        {then_body}\n    end else begin\n        {eb}\n    end"
                )
            }
            None => format!("if ({cond}) begin\n        {then_body}\n    end"),
        }
    }
    /// Emit an `initial` block.
    pub fn initial_block(&self, body: &str) -> String {
        format!("initial begin\n    {body}\nend")
    }
    /// Emit a local wire/reg/logic declaration.
    pub fn declare_signal(&self, ty: &VerilogType, name: &str) -> String {
        format!("{} {name};", self.emit_type(ty))
    }
    /// Emit an instance of another module.
    ///
    /// `port_map`: `&[("formal_name", "actual_signal")]`
    pub fn instantiate(
        &self,
        module_name: &str,
        instance_name: &str,
        port_map: &[(&str, &str)],
    ) -> String {
        let mut s = format!("{module_name} {instance_name} (\n");
        for (i, (formal, actual)) in port_map.iter().enumerate() {
            let comma = if i + 1 < port_map.len() { "," } else { "" };
            s.push_str(&format!("    .{formal}({actual}){comma}\n"));
        }
        s.push_str(");");
        s
    }
    /// Emit a `$display` / `$monitor` system task call.
    pub fn sys_task(&self, task: &str, fmt_str: &str, args: &[&str]) -> String {
        if args.is_empty() {
            format!("{task}(\"{fmt_str}\");")
        } else {
            let arg_str = args.join(", ");
            format!("{task}(\"{fmt_str}\", {arg_str});")
        }
    }
}

impl VerilogX2Liveness {
    #[allow(dead_code)]
    pub fn new(n: usize) -> Self {
        Self {
            live_in: vec![Vec::new(); n],
            live_out: vec![Vec::new(); n],
            defs: vec![Vec::new(); n],
            uses: vec![Vec::new(); n],
        }
    }
    #[allow(dead_code)]
    pub fn live_in(&self, b: usize, v: usize) -> bool {
        self.live_in.get(b).map(|s| s.contains(&v)).unwrap_or(false)
    }
    #[allow(dead_code)]
    pub fn live_out(&self, b: usize, v: usize) -> bool {
        self.live_out
            .get(b)
            .map(|s| s.contains(&v))
            .unwrap_or(false)
    }
    #[allow(dead_code)]
    pub fn add_def(&mut self, b: usize, v: usize) {
        if let Some(s) = self.defs.get_mut(b) {
            if !s.contains(&v) {
                s.push(v);
            }
        }
    }
    #[allow(dead_code)]
    pub fn add_use(&mut self, b: usize, v: usize) {
        if let Some(s) = self.uses.get_mut(b) {
            if !s.contains(&v) {
                s.push(v);
            }
        }
    }
    #[allow(dead_code)]
    pub fn var_is_used_in_block(&self, b: usize, v: usize) -> bool {
        self.uses.get(b).map(|s| s.contains(&v)).unwrap_or(false)
    }
    #[allow(dead_code)]
    pub fn var_is_def_in_block(&self, b: usize, v: usize) -> bool {
        self.defs.get(b).map(|s| s.contains(&v)).unwrap_or(false)
    }
}

impl VerilogExtWorklist {
    #[allow(dead_code)]
    pub fn new(capacity: usize) -> Self {
        Self {
            items: std::collections::VecDeque::new(),
            present: vec![false; capacity],
        }
    }
    #[allow(dead_code)]
    pub fn push(&mut self, id: usize) {
        if id < self.present.len() && !self.present[id] {
            self.present[id] = true;
            self.items.push_back(id);
        }
    }
    #[allow(dead_code)]
    pub fn push_front(&mut self, id: usize) {
        if id < self.present.len() && !self.present[id] {
            self.present[id] = true;
            self.items.push_front(id);
        }
    }
    #[allow(dead_code)]
    pub fn pop(&mut self) -> Option<usize> {
        let id = self.items.pop_front()?;
        if id < self.present.len() {
            self.present[id] = false;
        }
        Some(id)
    }
    #[allow(dead_code)]
    pub fn is_empty(&self) -> bool {
        self.items.is_empty()
    }
    #[allow(dead_code)]
    pub fn len(&self) -> usize {
        self.items.len()
    }
    #[allow(dead_code)]
    pub fn contains(&self, id: usize) -> bool {
        id < self.present.len() && self.present[id]
    }
    #[allow(dead_code)]
    pub fn drain_all(&mut self) -> Vec<usize> {
        let v: Vec<usize> = self.items.drain(..).collect();
        for &id in &v {
            if id < self.present.len() {
                self.present[id] = false;
            }
        }
        v
    }
}

impl VerilogX2PassRegistry {
    #[allow(dead_code)]
    pub fn new() -> Self {
        Self::default()
    }
    #[allow(dead_code)]
    pub fn register(&mut self, c: VerilogX2PassConfig) {
        self.stats.push(VerilogX2PassStats::new());
        self.configs.push(c);
    }
    #[allow(dead_code)]
    pub fn len(&self) -> usize {
        self.configs.len()
    }
    #[allow(dead_code)]
    pub fn is_empty(&self) -> bool {
        self.configs.is_empty()
    }
    #[allow(dead_code)]
    pub fn get(&self, i: usize) -> Option<&VerilogX2PassConfig> {
        self.configs.get(i)
    }
    #[allow(dead_code)]
    pub fn get_stats(&self, i: usize) -> Option<&VerilogX2PassStats> {
        self.stats.get(i)
    }
    #[allow(dead_code)]
    pub fn enabled_passes(&self) -> Vec<&VerilogX2PassConfig> {
        self.configs.iter().filter(|c| c.enabled).collect()
    }
    #[allow(dead_code)]
    pub fn passes_in_phase(&self, ph: &VerilogX2PassPhase) -> Vec<&VerilogX2PassConfig> {
        self.configs
            .iter()
            .filter(|c| c.enabled && &c.phase == ph)
            .collect()
    }
    #[allow(dead_code)]
    pub fn total_nodes_visited(&self) -> usize {
        self.stats.iter().map(|s| s.nodes_visited).sum()
    }
    #[allow(dead_code)]
    pub fn any_changed(&self) -> bool {
        self.stats.iter().any(|s| s.changed)
    }
}
