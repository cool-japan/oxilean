impl TailRecOpt {
    /// Create a new pass with default configuration.
    pub fn new() -> Self {
        TailRecOpt {
            config: TailRecConfig::default(),
            report: TailRecReport::new(),
            fresh: FreshIds::new(10_000),
        }
    }
    /// Create a new pass with the given configuration.
    pub fn with_config(config: TailRecConfig) -> Self {
        TailRecOpt {
            config,
            report: TailRecReport::new(),
            fresh: FreshIds::new(10_000),
        }
    }
    /// Run the pass on a single function declaration.
    ///
    /// Returns the report; `extra_decls` contains any synthesized helper
    /// functions (e.g., accumulator versions).
    pub fn run(&mut self, decl: &mut LcnfFunDecl) -> (TailRecReport, Vec<LcnfFunDecl>) {
        self.report = TailRecReport::new();
        let mut extras: Vec<LcnfFunDecl> = Vec::new();
        if !decl.is_recursive {
            return (self.report.clone(), extras);
        }
        if self.config.transform_linear {
            let fn_var = if let Some(p) = decl.params.first() {
                p.id
            } else {
                LcnfVarId(0)
            };
            let mut converted = 0usize;
            let new_body = rewrite_tail_calls(
                std::mem::replace(&mut decl.body, LcnfExpr::Unreachable),
                &fn_var,
                &mut converted,
            );
            decl.body = new_body;
            if self.body_has_tailcall(&decl.body) {
                self.report.functions_transformed += 1;
                self.report.calls_eliminated += self.count_tailcalls(&decl.body);
            }
        }
        if self.config.introduce_accum {
            let param_names: Vec<String> = decl.params.iter().map(|p| p.name.clone()).collect();
            if has_non_tail_recursive_call(&decl.body, &decl.name, &param_names) {
                if let Some(helper) = try_introduce_accumulator(decl, &mut self.fresh) {
                    self.report.functions_transformed += 1;
                    self.report.calls_eliminated += 1;
                    extras.push(helper);
                }
            }
        }
        (self.report.clone(), extras)
    }
    /// Run the pass over a whole module (list of declarations).
    pub fn run_module(&mut self, decls: &mut Vec<LcnfFunDecl>) -> TailRecReport {
        let mut total_report = TailRecReport::new();
        let mut all_extras: Vec<LcnfFunDecl> = Vec::new();
        for decl in decls.iter_mut() {
            let (rep, extras) = self.run(decl);
            total_report.functions_transformed += rep.functions_transformed;
            total_report.calls_eliminated += rep.calls_eliminated;
            all_extras.extend(extras);
        }
        decls.extend(all_extras);
        total_report
    }
    pub(crate) fn body_has_tailcall(&self, expr: &LcnfExpr) -> bool {
        match expr {
            LcnfExpr::TailCall(..) => true,
            LcnfExpr::Let { body, .. } => self.body_has_tailcall(body),
            LcnfExpr::Case { alts, default, .. } => {
                alts.iter().any(|a| self.body_has_tailcall(&a.body))
                    || default.as_ref().is_some_and(|d| self.body_has_tailcall(d))
            }
            _ => false,
        }
    }
    pub(crate) fn count_tailcalls(&self, expr: &LcnfExpr) -> usize {
        match expr {
            LcnfExpr::TailCall(..) => 1,
            LcnfExpr::Let { body, .. } => self.count_tailcalls(body),
            LcnfExpr::Case { alts, default, .. } => {
                alts.iter()
                    .map(|a| self.count_tailcalls(&a.body))
                    .sum::<usize>()
                    + default.as_ref().map_or(0, |d| self.count_tailcalls(d))
            }
            _ => 0,
        }
    }
}

impl TRDominatorTree {
    #[allow(dead_code)]
    pub fn new(size: usize) -> Self {
        TRDominatorTree {
            idom: vec![None; size],
            dom_children: vec![Vec::new(); size],
            dom_depth: vec![0; size],
        }
    }
    #[allow(dead_code)]
    pub fn set_idom(&mut self, node: usize, idom: u32) {
        self.idom[node] = Some(idom);
    }
    #[allow(dead_code)]
    pub fn dominates(&self, a: usize, b: usize) -> bool {
        if a == b {
            return true;
        }
        let mut cur = b;
        loop {
            match self.idom[cur] {
                Some(parent) if parent as usize == a => return true,
                Some(parent) if parent as usize == cur => return false,
                Some(parent) => cur = parent as usize,
                None => return false,
            }
        }
    }
    #[allow(dead_code)]
    pub fn depth(&self, node: usize) -> u32 {
        self.dom_depth.get(node).copied().unwrap_or(0)
    }
}

